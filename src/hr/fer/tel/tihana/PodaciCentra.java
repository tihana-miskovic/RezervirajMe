package hr.fer.tel.tihana;

public class PodaciCentra {

	String imeC;
	String kvartC;
	String gradC;
	String zupanijaC;
	String opisC;
	String adresaC;
	String brojTelefonaC;
	String emailC, cijenaC;

	public String getCijenaC() {
		return cijenaC;
	}

	public void setCijenaC(String cijenaC) {
		this.cijenaC = cijenaC;
	}

	public PodaciCentra() {
		super();
	}

	public String getImeC() {
		return imeC;
	}

	public void setImeC(String imeC) {
		this.imeC = imeC;
	}

	public String getKvartC() {
		return kvartC;
	}

	public void setKvartC(String kvartC) {
		this.kvartC = kvartC;
	}

	public String getGradC() {
		return gradC;
	}

	public void setGradC(String gradC) {
		this.gradC = gradC;
	}

	public String getZupanijaC() {
		return zupanijaC;
	}

	public void setZupanijaC(String zupanijaC) {
		this.zupanijaC = zupanijaC;
	}

	public String getOpisC() {
		return opisC;
	}

	public void setOpisC(String opisC) {
		this.opisC = opisC;
	}

	public String getAdresaC() {
		return adresaC;
	}

	public void setAdresaC(String adresaC) {
		this.adresaC = adresaC;
	}

	public String getBrojTelefonaC() {
		return brojTelefonaC;
	}

	public void setBrojTelefonaC(String brojTelefonaC) {
		this.brojTelefonaC = brojTelefonaC;
	}

	public String getEmailC() {
		return emailC;
	}

	public void setEmailC(String emailC) {
		this.emailC = emailC;
	}

}
